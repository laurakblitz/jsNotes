// EQUAL
console.log('3' == 3); // true
console.log(3 == 3);
console.log('3' == '3');

// STRICT EQUAL
console.log('3' === 3); // false
console.log(3 === 3); // true

// NOT EQUAL
console.log('3' != 3);

// STRICT NOT EQUAL
console.log('3' !== 3);

// GREATER THAN
3 > 2;

// LESS THAN
2 < 3;

// GREATER THAN OR EQUAL TO
3 >= 2; 

// LESS THAN OR EQUAL TO
2 <= 3;

// AND
2 && 3

// OR
2 || 3

